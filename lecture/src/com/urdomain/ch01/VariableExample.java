package com.urdomain.ch01;

public class  VariableExample  {
	 public static void main (String[]  args) {
		 //10을 변수 value의 초기갑으로 저장
		 int value= 10;
		 
		 
		 //변수 value 갑을 읽고 10을 더하는 산술 연산을 수행
		 //연산의 결과갑을 변수 result의 초기갑으로 저장
		 int result = value + 10;
		 
		 //변수 result 갑을 읽고 콘솔에 출력
		 System.out.println (result);
	 }
}